    val totalCorrect: Flow<Int?> = wordDao.getTotalCorrectAnswers()
    val totalWrong: Flow<Int?> = wordDao.getTotalWrongAnswers()

    suspend fun initializeWords() {
        if (wordDao.getCount() == 0) {
            wordDao.insertWords(getInitialWords())
        }
    }

    suspend fun getNextQuizWord(): WordEntity? {
        return wordDao.getNextQuizWord()
    }

    suspend fun getWrongAnswers(correctWord: WordEntity, count: Int = 5): List<WordEntity> {
        val words = wordDao.getRandomWordsExcluding(correctWord.id, count)
        return words
    }

    suspend fun onCorrectAnswer(word: WordEntity) {
        val updated = word.copy(
            correctCount = word.correctCount + 1,
            streak = word.streak + 1,
            lastAnsweredAt = System.currentTimeMillis(),
            isLearned = (word.streak + 1) >= 5 // Автоматически выучено после 5 правильных подряд
        )
        wordDao.updateWord(updated)
    }

    suspend fun onWrongAnswer(word: WordEntity) {
        val updated = word.copy(
            wrongCount = word.wrongCount + 1,
            streak = 0,
            lastAnsweredAt = System.currentTimeMillis()
        )
        wordDao.updateWord(updated)
    }

    suspend fun markAsLearned(wordId: Int) {
        wordDao.markAsLearned(wordId)
    }

    suspend fun markAsUnlearned(wordId: Int) {
        wordDao.markAsUnlearned(wordId)
    }

    suspend fun resetAllLearned() {
        wordDao.resetAllLearned()
    }

    suspend fun resetAllStats() {
        wordDao.resetAllStats()
    }

    // Для проверки выученных слов
    suspend fun getRandomLearnedWord(): WordEntity? {
        return wordDao.getRandomLearnedWord()
    }

    suspend fun getWrongAnswersForLearned(correctWord: WordEntity, count: Int = 5): List<WordEntity> {
        val words = wordDao.getRandomLearnedWordsExcluding(correctWord.id, count)
        if (words.size < count) {
            val additional = wordDao.getRandomWordsExcluding(correctWord.id, count - words.size)
            return (words + additional).take(count)
        }
        return words
    }

    fun getLearnedWordsShuffled(): Flow<List<WordEntity>> = wordDao.getLearnedWordsShuffled()

    // ===== CRUD =====

    suspend fun addUserWord(english: String, russian: String, category: String = "general"): Long {
        return wordDao.insertWord(
            WordEntity(
                english = english.trim(),
                russian = russian.trim(),
                category = category.ifBlank { "general" },
                isUserAdded = true
            )
        )
    }

    suspend fun updateUserWord(word: WordEntity) {
        wordDao.updateWord(word)
    }

    suspend fun deleteWord(word: WordEntity) {
        wordDao.deleteWord(word)
    }

    suspend fun getWordById(id: Int): WordEntity? = wordDao.getWordById(id)

